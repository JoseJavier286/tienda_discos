        <h3 id="utilizacion">Utilización de Cookies.</h3>
        <p class="uno" id="">
            La web AlcorPadel.com utiliza cookies y otros mecanismos similares (en adelante, Cookies). Una "Cookie" es un pequeño archivo que se almacena en el ordenador del 
            usuario y nos permite reconocerle. El conjunto de "cookies" nos ayuda a mejorar la calidad de nuestra web, permitiéndonos controlar qué páginas encuentran nuestros 
            usuarios útiles y cuáles no. Las cookies son esenciales para el funcionamiento de internet, aportando innumerables ventajas en la prestación de servicios 
            interactivos, facilitándole la navegación y usabilidad de nuestra web. Tenga en cuenta que las cookies no pueden dañar su equipo y que, a cambio, el que estén 
            activadas nos ayudan a identificar y resolver los errores.
        </p>
        <h3 id="tipologia">Tipología, finalidad y funcionamiento de las Cookies.</h3>
        <p class="dos">
            Las Cookies, en funcion de su permanencia, pueden dividirse en Cookies de sesion o permanentes.
        </p>
        <p class="dos">
            <b>Cookies de sesión:</b> son cookies temporales que permanecen en el archivo de cookies de su navegador hasta que abandone la pagina web, por lo que ninguna 
            queda registrada en el disco duro del usuario. La informacion obtenida por medio de estas cookies, sirven para analizar pautas de trafico en la web. 
            A la larga, esto nos permite proporcionar una mejor experiencia para mejorar el contenido y facilitando su uso.
        </p>
        <p class="dos">
            <b>Cookies permanentes: </b>son almacenadas en el disco duro y nuestra web las lee cada vez que usted realiza una nueva visita. Una web permanente posee una fecha de expiracion determinada. 
            La cookie dejara de funcionar despues de esa fecha. Las utilizamos, generalmente, para facilitar los servicios de compra y registro.
        </p>
        <p class="dos">Adicionalmente, en funcion de su objetivo, las Cookies puedes clasificarse de la siguiente forma:</p>
        <p class="dos">
            <b>Cookies de rendimiento:</b> Este tipo de Cookie recuerda sus preferencias para las herramientas que se encuentran en los Servicios, por lo que no tiene que volver a configurar el 
            servicio cada vez que usted visita. A modo de ejemplo, en esta tipologia se incluyen:
        </p>
        <ul class="lista">
            <li><p>Ajustes de volumen de reproductores de video o sonido.</p></li>
            <li><p>La resolución preferida en los vídeos.</p></li>
            <li><p>Las velocidades de transmision de video que sean compatibles con su navegador.</p></li>
        </ul>
        <p class="dos">
            <b>Cookies de Geo-Localizacion:</b> Estas Cookies son utilizadas para averiguar en que pais se encuentra cuando se solicita un Servicio. Esta Cookie es totalmente anonima, y solo se utiliza para ayudar 
            a orientar el contenido a su ubicacion. Cookies de registro: Las Cookies de registro se generan una vez que el Usuario se ha registrado o posteriormente ha abierto su sesion, y se utilizan para identificarle en los Servicios con los siguientes objetivos:
        </p>
        <ul class="lista">
            <li><p>Mantener al Usuario identificado de forma que, si cierra un Servicio, el navegador o el ordenador y en otro momento u otro dia vuelve a entrar en dicho Servicio, seguira identificado, facilitando asi su navegacion sin tener que volver a identificarse. Esta funcionalidad se puede suprimir si el Usuario pulsa la funcionalidad "cerrar sesion", de forma que esta Cookie se elimina y la proxima vez que entre en el Servicio el Usuario tendra que iniciar sesion para estar identificado</p></li>
            <li><p>Comprobar si el Usuario esta autorizado para acceder a ciertos Servicios, por ejemplo, para participar en un concurso.</p></li>
        </ul>
        <p class="dos">
            <b>Cookies de analiticas:</b> Cada vez que un Usuario visita un Servicio, una herramienta de un proveedor externo (Omniture, Netscope, Comscore y similares que podran anadirse a este listado en caso de que varien en relacion con los actuales) genera una Cookie
            analitica en el ordenador del Usuario. Esta Cookie que solo se genera en la visita, servira en proximas visitas a los Servicios de padelnuestro.com para identificar de forma anonima al visitante. Los objetivos principales que se persiguen son:
        </p>
        <ul class="lista">
            <li><p>Permitir la identificacion anonima de los Usuarios navegantes a traves de la "Cookie" (identifica navegadores y dispositivos, no personas) y por lo tanto la contabilizacion aproximada del numero de visitantes y su tendencia en el tiempo.</p></li>
            <li><p>Identificar de forma anonima los contenidos mas visitados y por lo tanto mas atractivos para los Usuarios.</p></li>
            <li><p>Saber si el Usuario que esta accediendo es nuevo o repite visita.</p></li>
        </ul>
        <h3 id="descripcion">Descripción de cookies:</h3>
        <p class="dos"><b>Cookies de AlcorPadel</b></p>
        <p class="dos"><b>AlcorPadeluser: Autentificación usuario de AlcorPadel.</b></p>
        <p class="dos"><b>Google analytics:</b> Cookies __utmb, __utmc, __utmz y __utma: Permiten contar páginas vistas y Fuentes de tráfico para poder medir y mejorar el rendimiento de los sitios. Google almacena la información recogida por las cookies en los servidores de Estados Unidos. Google puede transferir la información a terceros cuando lo requiera la legislación o cuando dichos terceros procesan la información por parte de Google. 
        Google no asociará su dirección IP con ningún otro dato del que disponga Google.</p>
        <h3 id="gestion">Gestión de Cookies:</h3>
        <p class="dos">Como garantía complementaria a las anteriormente descritas, el registro de las cookies podrá estar sujeto a su aceptación durante la instalación o puesta al día del navegador usado, y esta aceptación puede en todo momento ser revocada mediante las opciones de configuración de contenidos y privacidad disponibles.</p>
        <p class="dos">Muchos navegadores permiten activar un modo privado mediante el cual las cookies se borran siempre después de su visita. Dependiendo de cada navegador este modo privado, puede tener diferentes nombres. A continuación encontrará una lista de los navegadores más comunes y los diferentes nombres de este "modo privado":</p>
        <ul class="lista">
            <li><p>Internet Explorer 8 y superior; InPrivate</p></li>
            <li><p>Opera 10.5 y superior; Navegación Privada</p></li>
            <li><p>Google Chrome 10 y superior; Incógnito</p></li>
            <li><p>Safari 2 y superior; Navegación Privada</p></li>
            <li><p>FireFox 3.5 y superior; Navegación Privada</p></li>
        </ul>
        <h3 id="deshabilitar">Como deshabilitar las Cookies en los principales navegadores:</h3>
        <p class="dos">Normalmente es posible dejar de aceptar las Cookies del navegador, o dejar de aceptar las Cookies de un Servicio en particular.</p>
        <p class="dos">Todos los navegadores modernos permiten cambiar la configuracion de Cookies. Estos ajustes normalmente se encuentra en las opciones o preferencias del menu de su navegador. Asimismo, puede configurar su navegador o su gestor de correo electronico, asi como instalar complementos gratuitos para evitar que se descarguen los Web Bugs al abrir un email.</p>
        <p class="dos">Le ofrecemos orientacion al Usuario sobre los pasos para acceder al menu de configuracion de las cookies y, en su caso, de la navegacion privada en cada uno de los navegadores principales:</p>
        <p class="dos">Internet Explorer: Herramientas -> Opciones de Internet -> Privacidad -> Configuracion. Firefox: Herramientas -> Opciones -> Privacidad -> Historial -> Configuracion Personalizada. Chrome: Configuracion -> Mostrar opciones avanzadas -> Privacidad -> Configuracion de contenido. Safari: Preferencias -> Seguridad.</p>
